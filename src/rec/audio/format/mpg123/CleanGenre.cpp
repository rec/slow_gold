string cleanGenre(int32 index) {
    return (index >= 0 && index < GENRE_COUNT) ? GENRES[index] : "";
}

string cleanGenre(const string& s) {
    string result(s);
    if (!result.empty()) {
        while (*(result.begin()) == '(' && *(result.rbegin()) == ')')
            result = result.substr(1, result.size() - 2);
        if (isdigit(result[0]))
            result = cleanGenre(str(result).getIntValue());
        if (result.empty())
            result = s;
    }
    return result;
}

